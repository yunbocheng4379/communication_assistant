SCENE_DETECTION_SYSTEM_PROMPT = """你是一个专业的需求分析助手。你的任务是根据用户输入的内容，判断它更接近产品经理的思维方式还是开发工程师的思维方式。

【核心判断原则】
不要返回 "unknown"！除非内容完全是随机字符或无法理解的语言，否则必须做出判断。

【开发工程师视角关键词】（高优先级，只要出现就强烈倾向开发）
- 技术操作类：上线、部署、发布、构建、打包、发布流程、回滚、灰度
- 技术实现类：接口、API、数据库、Redis、缓存、MQ、队列、微服务
- 代码相关：代码、类、方法、函数、模块、设计、重构、优化性能
- 系统运维类：服务器、容器、集群、监控、告警、容灾、熔断、降级
- 架构设计类：架构、分布式、高并发、负载均衡、CDN、缓存策略

【产品经理视角关键词】（高优先级，只要出现就强烈倾向产品）
- 业务目标类：用户增长、留存、转化率、DAU、MAU、GMV、用户体验
- 功能描述类：功能、需求、页面、按钮、弹窗、表单、列表、详情页
- 业务流程类：支付流程、审核流程、下单流程、注册流程、引导流程
- 商业价值类：ROI、用户满意度、市场竞争力、业务增长、产品价值

【决策规则】
1. 强特征优先：出现开发关键词（如"上线"、"部署"、"接口"）→ 开发工程师
2. 强特征优先：出现产品关键词（如"用户体验"、"转化率"、"功能"）→ 产品经理
3. 模糊场景判断：
   - "需要XX功能/页面/模块" + 无技术细节 → 产品经理
   - "需要进行XX操作/上线/部署" + 无业务背景 → 开发工程师
   - "开了一个新的XX" + "需要XX操作" → 偏向开发（因为"操作"是工程用语）
4. 纯动作描述：动词+名词但无上下文，通常是开发（开发习惯描述操作步骤）

【输出要求】
直接输出一个单词，不要任何解释：
- 如果判断为产品经理视角：输出 "product"
- 如果判断为开发工程师视角：输出 "dev"

不要输出 "unknown"，不要输出其他任何内容。"""

SCENE_DETECTION_USER_TEMPLATE = """【待判断内容】

{input_text}

【请判断这是产品经理还是开发工程师的视角？】"""

# ============================================================================
# 提示词辅助函数
# ============================================================================

def get_product_to_dev_prompts(input_text: str) -> tuple:
    """
    获取产品经理视角翻译所需的系统提示词和用户提示词
    
    Args:
        input_text: 产品经理输入的需求描述
        
    Returns:
        (system_prompt, user_prompt) 元组
    """
    return (
        PRODUCT_TO_DEV_SYSTEM_PROMPT,
        PRODUCT_TO_DEV_USER_TEMPLATE.format(input_text=input_text)
    )

def get_dev_to_product_prompts(input_text: str) -> tuple:
    """
    获取开发工程师视角翻译所需的系统提示词和用户提示词
    
    Args:
        input_text: 开发工程师输入的技术方案描述
        
    Returns:
        (system_prompt, user_prompt) 元组
    """
    return (
        DEV_TO_PRODUCT_SYSTEM_PROMPT,
        DEV_TO_PRODUCT_USER_TEMPLATE.format(input_text=input_text)
    )

def get_scene_detection_prompts(input_text: str) -> tuple:
    """
    获取场景识别所需的提示词
    
    Args:
        input_text: 待判断的内容
        
    Returns:
        (system_prompt, user_prompt) 元组
    """
    return (
        SCENE_DETECTION_SYSTEM_PROMPT,
        SCENE_DETECTION_USER_TEMPLATE.format(input_text=input_text)
    )
